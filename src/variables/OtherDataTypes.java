package variables;

public class OtherDataTypes {
    public static void main(String[] args) {
        // Chart" is a data type that only accepts a single character.
        char letter = 'a';

        // Bolean
        boolean isActive = true;
        boolean isAdmin = false;

        /*
        * Var --> can be used in Java starting from version 10 and serves as a keyword
        * for declaring variables without specifying the type, as it is inferred.
        * */
        var fullName = "James Gosling";
        var age = 25;
        var weight = 74.56;
        var isEmployee = false;
        var letterId = 'B';
    }
}
