package variables;

public class DataTypes {
    public static void main(String[] args) {
        // Numeric data types
        // byte -128 --> 127
        byte smallNumber = 127;

        // short -32,768 --> 32,767
        short shortNumber = 32767;

        // int -2,147,483,648 --> 2,147,483,647
        int normalNumber = 2147483647;

        // long -9,223,372,036,854,775,808 --> 9,223,372,036,854,775,807
        long longNumber = 9223372036854775807L;

        // float 1.40129846432481707e-45 --> 3.4028234663858860e38
        float normalDecimal = 5.154874558F;

        // double 4.94065645841246544e-324d --> 1.79769313486231570e308d
        double longDecimal = 5478.9862214588555;
    }
}
