package variables;

public class BasicVariables {
    public static void main(String[] args) {
        /*
        * As a good rule of thumb, in Java, always make
        * sure to initialize variables when you create them.
        * */
        int speed = 80;
        String employeeName = "Carlos Cruz";

        System.out.println("The speed is " + speed);
        System.out.println("Employee name is " + employeeName);
    }
}
